//  Built-in function layout
//
//  .-------------------------------------------------------------> open bif
//  | .-----------------------------------------------------------> modifier
//  | |   .-------------------------------------------------------> bif name
//  | |   |   .---------------------------------------------------> bif name separator
//  | |   |   |      .--------------------------------------------> bif params
//  | |   |   |      |       .------------------------------------> params / code separator
//  | |   |   |      |       |             .----------------------> comment
//  | |   |   |      |       |             |   .------------------> bif code
//  | |   |   |      |       |             |   |               .--> close bif
//  | |   |   |      |       |             |   |               |
//  v v   v   v      v       v             |   v               v
//  - - ----- - ------------ -- -----------v------------------ --
//  {:!snippet; snippet_name >> <div>... {:* ... *:} ...</div> :}
//  -------------------------------------------------------------
//          ^  -----------------------------------------------
//          |                      ^
//          |                      |
//          |                      ·------------------------------> bif src
//          ·-----------------------------------------------------> bif: Built-in function

